/**
 * Laço WHILE
 * percorre um bloco de código
 * desde que uma condição especificada seja verdadeira.
 */
var i = 0
while(i < 400){  //condiçao do laço
    console.log("Estou em: " + i); //executando a açao do laço
    i++; //incremento do laço
}

var i = 1
while(++i < 100){ //condiçao e incremento combinados
    console.log('REsult ' + i); 
    
}